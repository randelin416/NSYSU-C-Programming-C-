int main(void) {
	//宣告變數(浮點數) 
	float a, b;
	//輸入三角形底之值 
	printf("請輸入三角形的底邊長度：");
	scanf("%f", &a);
	//輸入三角形高之值 
	printf("\n請輸入三角形的高度：");
	scanf("%f", &b);
	//計算三角形面積(底*高/2) 
	printf("\n三角形的面積為：%f", a*b/2); 
}
